package batch63_GioR;

public class DS1_8 {

	public static void main(String[] args) { // check if char is vowel or consonant
		char c = 'o';
		
		if(c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u') {
			System.out.println(c + " is a vowel");
		}
		else {
			System.out.println(c + " is a consonant");
		}

	}

}
