package batch63_GioR;

public class DS1_4 {

	public static void main(String[] args) { // swap two numbers
		int a = 213;
		int b = 843;
		System.out.println("Normal numbers: ");
		System.out.println("a: " + a);
		System.out.println("b: " + b);
		int temp = 0;
		temp = a;
		a = b;
		b = temp;
		System.out.println("Swapped numbers: ");
		System.out.println("a: " + a);
		System.out.println("b: " + b);

	}

}
